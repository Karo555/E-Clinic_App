package com.example.e_clinic_app.data.users

/**
 * Enum representing the roles of users in the e-clinic application.
 *
 * This enum defines the different roles that a user can have within the system,
 * such as `PATIENT`, `DOCTOR`, and `ADMIN`.
 */
enum class Role {
    /** Represents a patient user in the system. */
    PATIENT,
    /** Represents a doctor user in the system. */
    DOCTOR,
    /** Represents an admin user in the system. */
    ADMIN
}